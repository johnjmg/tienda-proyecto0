    void consultar (){
        String sql = "select * from comprador";
        
        try {
            conet = con1.getConnection();
            st = conet.createStatement();
            rs =st.executeQuery(sql);
            Object [] comprador = new Object[5];
            modelo = (DefaultTableModel) TablaRegistroBD.getModel();
            while (rs.next()){
                comprador [0] = rs.getInt("idComprador");
                comprador [1] = rs.getString("nombre");
                comprador [2] = rs.getString("dirección");
                comprador [3] = rs.getInt("teléfono");
                comprador [4] = rs.getString("correo");
                
                modelo.addRow(comprador);
            }
            TablaRegistroBD.setModel(modelo);
        }catch(Exception e){
            
        }
    }
    
    void Agregar (){
        String nom = TextNOMBRE.getText();
        String dir = TextDIRECCION.getText();
        String tel = TextTELEFONO.getText();
        String correo = TextCORREO.getText();
        
        try {
            if (nom.equals("") || dir.equals("") || tel.equals("") || correo.equals("")){
                JOptionPane.showMessageDialog(null,"Debe ingresar datos");
                limpiarTabla();
        }else {
                String sql ="insert into comprador(nombre, dirección, teléfono, correo) values ('"+nom+"','"+dir+"','"+tel+"','"+correo+"')";
                conet = con1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Datos ingresados exitosamente");
                limpiarTabla();
            }
        }catch(Exception e){
            
        }
    }
    
    void limpiarTabla(){
        for (int i = 0; i<= TablaRegistroBD.getRowCount();i++){
            modelo.removeRow(i);
            i=i-1;
        }
    }
    
    void Eliminar (){
        int fila =TablaRegistroBD.getSelectedRow();
        try{
           if (fila < 0){
               JOptionPane.showMessageDialog(null, "Dato no seleccionado");
               limpiarTabla();
           }else {
               String sql = "delete from comprador where idComprador="+idc;
               conet = con1.getConnection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Datos eliminados exitosamente");
                limpiarTabla();
           } 
        }catch (Exception e){
        
        }
    }
    
