    private void TablaRegistroBDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaRegistroBDMouseClicked
        int fila = TablaRegistroBD.getSelectedRow();
        if (fila == -1){
           JOptionPane.showMessageDialog(null, "No se seleccionó fila");
        }else{
            idc = Integer.parseInt((String) TablaRegistroBD.getValueAt(fila, 0) .toString());
            String nom =(String) TablaRegistroBD.getValueAt(fila, 1);
            String dir =(String) TablaRegistroBD.getValueAt(fila, 2);
            int tel = Integer.parseInt((String) TablaRegistroBD.getValueAt(fila, 3) .toString());
            String correo =(String) TablaRegistroBD.getValueAt(fila, 4);
            
            TextID.setText("" +idc);
            TextNOMBRE.setText(nom);
            TextDIRECCION.setText(""+dir);
            TextTELEFONO.setText(""+tel);
            TextCORREO.setText(""+correo);
        }
    }//GEN-LAST:event_TablaRegistroBDMouseClicked

